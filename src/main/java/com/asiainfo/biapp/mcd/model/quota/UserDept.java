package com.asiainfo.biapp.mcd.model.quota;

public class UserDept {
	
	private String cityId;
	private String deptId;
	private String deptName;
	private String deptDesc;
	private String deptDisplayOrder;
	
	public String getCityId() {
		return cityId;
	}
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	public String getDeptId() {
		return deptId;
	}
	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getDeptDesc() {
		return deptDesc;
	}
	public void setDeptDesc(String deptDesc) {
		this.deptDesc = deptDesc;
	}
	public String getDeptDisplayOrder() {
		return deptDisplayOrder;
	}
	public void setDeptDisplayOrder(String deptDisplayOrder) {
		this.deptDisplayOrder = deptDisplayOrder;
	}

}

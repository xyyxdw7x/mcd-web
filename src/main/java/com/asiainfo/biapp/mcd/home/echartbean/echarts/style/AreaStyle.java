package com.asiainfo.biapp.mcd.home.echartbean.echarts.style;



public class AreaStyle {
	private String color;
	private String type;
	private String size;
	
	public String getColor() {
		return color;
	}
	public AreaStyle setColor(String color) {
		this.color = color;
		return this;
	}
	public String getType() {
		return type;
	}
	public AreaStyle setType(String type) {
		this.type = type;
		return this;
	}
	public String getSize() {
		return size;
	}
	public AreaStyle setSize(String size) {
		this.size = size;
		return this;
	}
	
	
}

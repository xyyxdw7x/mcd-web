public class Grid {	
	private String x;
	private String y;
	private String x2;
	private String y2;
	private String width;
	private String height;
	private String backgroundColor;
	private Integer borderWidth;
	private String borderColor;
	
	public String getX() {
		return x;
	}
	public Grid setX(String x) {
		this.x = x;
		return this;
	}
	public String getY() {
		return y;
	}
	public Grid setY(String y) {
		this.y = y;
		return this;
	}
	public String getX2() {
		return x2;
	}
	public Grid setX2(String x2) {
		this.x2 = x2;
		return this;
	}
	public String getY2() {
		return y2;
	}
	public Grid setY2(String y2) {
		this.y2 = y2;
		return this;
	}
	public String getWidth() {
		return width;
	}
	public Grid setWidth(String width) {
		this.width = width;
		return this;
	}
	public String getHeight() {
		return height;
	}
	public Grid setHeight(String height) {
		this.height = height;
		return this;
	}
	public String getBackgroundColor() {
		return backgroundColor;
	}
	public Grid setBackgroundColor(String backgroundColor) {
		this.backgroundColor = backgroundColor;
		return this;
	}
	public Integer getBorderWidth() {
		return borderWidth;
	}
	public Grid setBorderWidth(Integer borderWidth) {
		this.borderWidth = borderWidth;
		return this;
	}
	public String getBorderColor() {
		return borderColor;
	}
	public Grid setBorderColor(String borderColor) {
		this.borderColor = borderColor;
		return this;
	}
	
	
}

package com.asiainfo.biapp.mcd.common.echartBean.echarts.series.util;


public class Pointer {
 	
	private String length;
	private Integer width;
	private String color;
	
	public String getLength() {
		return length;
	}
	public Pointer setLength(String length) {
		this.length = length;
		return this;
	}
	public Integer getWidth() {
		return width;
	}
	public Pointer setWidth(Integer width) {
		this.width = width;
		return this;
	}
	public String getColor() {
		return color;
	}
	public Pointer setColor(String color) {
		this.color = color;
		return this;
	}
	
	
}
